Meaning (find purpose in the struggle)
Prayer or meditation
Relaxation (progressive muscle relaxation, deep breathing)
One thing at a time (focus on the present moment)
Vacation (brief mental break)
Encouragement (self-compassionate self-talk)

Practice:
