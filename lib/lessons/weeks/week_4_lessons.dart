Breaking the Chain:
You can't always control the trigger or the initial emotion, but you can change your thoughts and behavior.

Instead of:
• Emotion: Anger → Thought: "I can't handle this" → Behavior: Use

Try:
• Emotion: Anger → Thought: "This is hard, but I can tolerate it" → Behavior: Deep breathing, call a friend, go for a walk

Tolerating Emotions:
Recovery means learning to sit with uncomfortable emotions instead of numbing them. It's not about making them go away—it's about surviving them without using.

The more you practice tolerating discomfort, the easier it becomes.
