Not all relationships are worth saving. Some are actively harmful to your recovery.

Learning to recognize toxic relationships—and having the courage to walk away—is critical.

What Makes a Relationship Toxic?
• They pressure you to use or undermine your recovery
• They're consistently dishonest or manipulative
• They take without giving (one-sided)
• They're verbally, emotionally, or physically abusive
• They refuse to respect your boundaries
• Being around them makes you feel worse about yourself

Enabling vs. Supporting:
Enabling means protecting someone from the consequences of their actions. It keeps them stuck.

Supporting means holding someone accountable while offering love and encouragement.

If someone in your life enables your substance use or makes excuses for you, they're part of the problem, even if they love you.

Codependency:
This is when your sense of self becomes tangled up in another person. You lose yourself trying to fix or save them.

Signs of codependency:
• Putting others' needs above your own, always
• Feeling responsible for others' feelings
• Difficulty saying no
• Defining yourself by your relationships
• Fear of being alone

Codependent relationships often feel intense and important, but they're not healthy.

When to Distance or Leave:
If a relationship consistently threatens your sobriety, you need to create distance or end it entirely.

This is incredibly hard, especially if it's family or a long-term relationship. But your life depends on it.

You can love someone and still choose not to have them in your life.
