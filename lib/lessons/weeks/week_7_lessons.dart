Healthy communication is essential for maintaining relationships and resolving conflict without turning to substances.

Many people in recovery never learned how to communicate effectively.

"I" Statements:
"I" statements express your feelings and needs without blaming others.

Instead of: "You never listen to me!"
Try: "I feel unheard when I'm interrupted."

Instead of: "You're making me stressed!"
Try: "I feel stressed when plans change last minute."

"I" statements reduce defensiveness and open the door to productive conversation.

Active Listening:
This means truly hearing what someone is saying, not just waiting for your turn to talk.

• Make eye contact
• Nod and show you're engaged
• Reflect back what you heard ("It sounds like you're feeling...")
• Ask clarifying questions
• Don't interrupt or offer unsolicited advice

Assertive Communication:
Assertiveness is the balance between passive (avoiding conflict) and aggressive (attacking others).

Assertive communication is:
• Clear and direct
• Respectful of yourself and others
• Honest without being harsh
• Firm but kind

Example: "I understand you're upset, but I won't continue this conversation if you're yelling. Let's talk when we've both calmed down."

Conflict Is Not the Enemy:
Healthy relationships have conflict. The goal is not to avoid conflict but to navigate it without destruction.
