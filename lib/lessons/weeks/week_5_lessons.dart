Grounding techniques help you return to the present moment when you're overwhelmed by trauma memories, flashbacks, or intense emotions.

These are essential tools for trauma survivors in recovery.

Why Grounding Works:
Trauma pulls you into the past. Your body reacts as if the traumatic event is happening now.

Grounding reminds your nervous system: "I am here, now. I am safe."

Sensory Grounding (5-4-3-2-1):
This uses your five senses to anchor you in the present.
• 5 things you can see
• 4 things you can touch
• 3 things you can hear
• 2 things you can smell
• 1 thing you can taste

Physical Grounding:
• Feel your feet on the floor
• Press your hands together
• Hold ice or splash cold water on your face
• Stretch or move your body
• Touch something textured (soft blanket, rough stone)

Mental Grounding:
• Name objects in the room
• Count backward from 100 by 7s
• Recite something you've memorized
• Describe your surroundings in detail

Emotional Grounding:
• Say out loud: "I am [name]. I am [age]. I am in [location]. I am safe."
• Remind yourself: "That was then. This is now."

Practice These When You're Calm:
Don't wait for a crisis to learn grounding. Practice when you're regulated so the techniques are automatic when you need them.
