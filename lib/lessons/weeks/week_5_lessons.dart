Trauma-informed recovery recognizes that healing is not linear and that you need to go at your own pace.

There is no timeline for healing. There is no "right way" to recover.

What Trauma-Informed Means:
• Safety first: You need to feel safe before you can heal
• Choice and control: You decide what you're ready for
• Collaboration: You work with support, not under someone's control
• Trustworthiness: Your support system is consistent and reliable
• Empowerment: You are the expert on your own experience

Healing Is Not Forgetting:
Trauma-informed recovery is not about erasing what happened. It's about integrating the experience so it no longer controls you.

You will always remember what happened. But the memory won't always have the same power over you.

Self-Compassion in Trauma Recovery:
Trauma survivors often blame themselves for what happened or for struggling to heal.

Self-compassion means understanding that:
• What happened to you was not your fault
• Your survival strategies made sense
• Healing takes as long as it takes
• You are worthy of care and safety

Be Patient with Yourself:
Trauma healing is slow, non-linear work. You will have setbacks. You will have days where you feel like you're back at the beginning.

That's normal. That's part of healing.

