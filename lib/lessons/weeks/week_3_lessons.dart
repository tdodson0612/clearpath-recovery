What Are Cognitive Distortions?
Cognitive distortions are patterns of thinking that are exaggerated, irrational, or simply untrue. They feel real in the moment, but they don't reflect reality.

Examples:
• "I'll never be able to stay sober" (fortune telling)
• "Everyone thinks I'm a failure" (mind reading)
