Catastrophizing:
This is when you imagine the worst-case scenario and treat it as inevitable.

Examples:
• "If I go to that party, I'll definitely relapse"
• "If I tell my family about my recovery, they'll reject me"
• "One craving means I'm going to lose everything"

The problem: You can't predict the future. Treating worst-case scenarios as certainties creates unnecessary fear.

Mind Reading:
This is when you assume you know what others are thinking about you—and it's usually negative.

Examples:
• "Everyone at the meeting thinks I'm pathetic"
• "My boss knows I'm in recovery and thinks less of me"
• "My family will never forgive me"

The problem: You don't actually know what others think. You're projecting your own fears onto them.

Challenging These Distortions:
Ask yourself: "What evidence do I have for this belief?"
Consider: "What's a more realistic outcome?"
Remember: You can't read minds, and the future isn't written yet.
