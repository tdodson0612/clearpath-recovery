"Should" statements and labeling are two of the most harmful cognitive distortions because they attack your sense of self.

Should Statements:
These are rigid rules you impose on yourself or others.

Examples:
• "I should be further along in recovery by now"
• "I should be able to handle stress without struggling"
• "I shouldn't need help"

The problem: "Should" statements create guilt and shame. They ignore reality and set impossible standards.

Better alternative: Replace "should" with "I choose" or "I want."
• "I choose to work on my recovery at my own pace"
• "I want to develop better coping skills"

Labeling:
This is when you define yourself by a mistake or characteristic.

Examples:
• "I'm an addict" (instead of "I have an addiction")
• "I'm a failure" (instead of "I made a mistake")
• "I'm broken" (instead of "I'm healing")

The problem: Labels become identity. When you label yourself negatively, you reinforce that belief.

Better alternative: Separate your behavior from your identity.
• "I struggled today" (not "I'm weak")
• "I made a poor choice" (not "I'm a bad person")

You are not your mistakes. You are not your diagnosis. You are a person in recovery, learning and growing.
''',
