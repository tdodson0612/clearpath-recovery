How Thoughts Influence Behavior:
Your thoughts create emotions, and emotions drive behavior. If you think "I can't handle this," you'll feel hopeless, and you're more likely to use substances to cope.

But if you can challenge that thought—"This is hard, but I've handled hard things before"—you change the emotion and the behavior.

The Goal:
You don't need to eliminate negative thoughts. You need to recognize when they're distorted and choose not to believe them.
