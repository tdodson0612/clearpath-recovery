• Things you did while using
• People you hurt
• Time you wasted
• Potential you lost

Self-forgiveness means:
• Acknowledging the harm you caused
• Taking accountability without drowning in shame
• Understanding you were sick, not evil
• Committing to do better going forward

You can't change the past. You can only choose who you'll be from this moment on.

Letting Go:
Forgiveness is a process of letting go. It doesn't happen in one moment.

It's a decision you make again and again: "I release this. I choose peace over resentment."
