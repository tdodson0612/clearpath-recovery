Mindfulness is the practice of being fully present in the current moment without judgment.

It's one of the most powerful tools in recovery.

What Mindfulness Is (and Isn't):
Mindfulness is:
• Noticing what's happening right now
• Observing without judging
• Being with what is, not what was or will be

Mindfulness is NOT:
• Clearing your mind of all thoughts
• Forcing yourself to feel calm
• Spiritual bypass (avoiding problems with "positive vibes")

Why Mindfulness Matters in Recovery:
Addiction keeps you stuck in the past (regret, shame) or future (worry, fear). Mindfulness brings you back to now.

And in this moment, right now, you are okay.

Practicing Mindfulness:

1. Mindful Breathing
Notice each inhale and exhale. When your mind wanders, gently return to the breath.

2. Body Scan
Bring awareness to each part of your body from head to toe.

3. Mindful Walking
Notice each step. Feel your feet on the ground.

4. Mindful Eating
Taste each bite. Notice texture, flavor, temperature.

5. Observation
Notice 5 things you can see, hear, feel, smell, or taste.

Acceptance Without Judgment:
Mindfulness teaches you to observe thoughts and feelings without labeling them as good or bad.

You can notice a craving without acting on it. You can feel anxiety without being consumed by it.

This creates space between stimulus and response—the space where you choose how to act.
